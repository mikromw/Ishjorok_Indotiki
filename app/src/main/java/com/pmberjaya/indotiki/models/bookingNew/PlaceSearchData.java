public class PlaceSearchData {
    String place_id;
    String favorite;
    String id_favorite;
    String place;
    String place_details;
    String lat;
    String lng;
    String distance;

    public String getPlace_id() {
        return place_id;
    }

    public void setPlace_id(String place_id) {
        this.place_id = place_id;
    }

    public String getFavorite() {
        return favorite;
    }

    public void setFavorite(String favorite) {
        this.favorite = favorite;
    }

    public String getId_favorite() {
        return id_favorite;
    }

    public void setId_favorite(String id_favorite) {
        this.id_favorite = id_favorite;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getPlace_details() {
        return place_details;
    }

    public void setPlace_details(String place_details) {
        this.place_details = place_details;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getDistance() {
        return distance;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }
}
